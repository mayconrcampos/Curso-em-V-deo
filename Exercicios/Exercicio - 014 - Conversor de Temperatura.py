c = float(input("Informe a temperatura em Celsius: "))
f = 9 * c / 5 + 32 #Este cálculo não precisou de parenteses pois os cálculos estão na ordem de precedência
print("A temperatura {} ºC é equivalente a {} ºF".format(c, f))

